/**
 * Seed localStorage with sample data
 * Only adds entries that don't already exist (by ID)
 * Merges with existing data without creating duplicates
 * @returns {object} Result with success status and message
 */
export function seedSampleData() {
	try {
		const STORAGE_KEY = "salarytrack_entries_v1";
		const existing = localStorage.getItem(STORAGE_KEY);

		let currentEntries = [];
		if (existing) {
			currentEntries = JSON.parse(existing);
		}

		// Get IDs of existing entries
		const existingIds = new Set(currentEntries.map((e) => e.id));

		// Filter sample data to only include new entries
		const newEntries = SAMPLE_DATA.filter((entry) => !existingIds.has(entry.id));

		if (newEntries.length === 0) {
			return {
				success: false,
				message: `All sample entries already exist! (${currentEntries.length} total entries)`,
			};
		}

		// Merge existing + new entries and save
		const mergedEntries = [...currentEntries, ...newEntries];
		localStorage.setItem(STORAGE_KEY, JSON.stringify(mergedEntries));

		return {
			success: true,
			message: `Added ${newEntries.length} new entries! (${mergedEntries.length} total)`,
		};
	} catch (error) {
		console.error("Error seeding sample data:", error);
		return {
			success: false,
			message: `Error loading data: ${error.message}`,
		};
	}
}
